using IbmcKvm.Core.Video;

namespace IbmcKvm.Core.Tests.Video;

public sealed class JpegHeaderFactoryTests
{
    [Theory]
    [InlineData(0, "670570D11E2C1DFF88031CFF95BFA1841D674348B7BC8C1FE7AF0DD3FF1CA2F3")]
    [InlineData(1, "B0F8CFE1FE62ED4BEFCA1F74F2D2B9E16BFD489A866BBFE5C089AE04B0CF619E")]
    [InlineData(2, "C54E428A7BDABC4884613EBEB934D44EAA99DB2B8486B86B88D5C51B715C1C24")]
    [InlineData(3, "BDEF897EF3E45909B4CEB70CF42820AE00CABFC2B6C3EF38AB2F9BEAFD9D565C")]
    [InlineData(4, "76577CC1BC4A3FAE7998F949D4900C469728D5D83F7774EA75F5A27A85CD56DE")]
    [InlineData(5, "D53E7E514A26319DDFD9DFBDCF2E8EB7B4079F9E90C2FAA5EB9D8D6ADC75A937")]
    [InlineData(6, "2AFCDB98727EB3E86F60719557106190EE1697F7F5B7E52CB3B5DD63F82F251D")]
    [InlineData(7, "F75407BB3A65FA4E75C7CE029889BD97124465C2F0555075D7DE52E7B64B7642")]
    [InlineData(8, "7AD5BDFBCD88CC3F9DF9EB77A65A354294EFC0493E91FD90BBC6BAD772B2B79D")]
    [InlineData(9, "F549FDDE39FCFC305079BEC177D898A5F7C160A6959F558FE2D7479B3220BCD4")]
    public void MatchesKnownJpegHeaderVector(int tableIndex, string expectedSha256)
    {
        var header = JpegHeaderFactory.Create(tableIndex);

        Assert.Equal(698, header.Length);
        Assert.Equal(expectedSha256, Convert.ToHexString(SHA256.HashData(header)));
    }
}
